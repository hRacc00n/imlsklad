import re

def normalize_comment(text):
    """
    Нормализация текста: расстановка пробелов.
    """
    if not text:
        return text
    
    result = text
    
    # 1. Пробел после точки, если после нее идет буква (не в дате)
    result = re.sub(r'\.([А-Яа-яA-Za-z])', r'. \1', result)
    
    # 2. Пробел после точки с запятой
    result = re.sub(r';([А-Яа-яA-Za-z])', r'; \1', result)
    
    # 3. Пробел после запятой
    result = re.sub(r',([А-Яа-яA-Za-z])', r', \1', result)
    
    # 4. Пробел перед заглавной буквой (если перед ней строчная или цифра)
    result = re.sub(r'([а-яё0-9\)\]}])[ ]*([А-ЯЁA-Z])', r'\1 \2', result)
    
    # 5. Пробел перед аббревиатурами (СД, АО, ООО, ИП и т.д.)
    abbreviations = r'(SD|АО|ООО|ИП|ЗАО|ОАО|НДС|МРТ|КПП|ИНН|ОГРН|Гр|СПб|МТС)'
    result = re.sub(r'([^ ])(' + abbreviations + r')', r'\1 \2', result)
    
    # 6. Пробел после аббревиатуры, если после нее идет слово
    result = re.sub(r'(' + abbreviations + r')([А-Яа-яA-Za-z])', r'\1 \2', result)
    
    # 7. Пробел перед датой в формате ДД.ММ.ГГГГ
    result = re.sub(r'([^ ])(\d{2}\.\d{2}\.\d{4})', r'\1 \2', result)
    
    # 8. Пробел после даты
    result = re.sub(r'(\d{2}\.\d{2}\.\d{4})([А-Яа-яA-Za-z])', r'\1 \2', result)
    
    # 9. Пробел перед датой в формате ДД/ММ/ГГГГ или ДД/ММ/ГГ
    result = re.sub(r'([^ ])(\d{2}/\d{2}/\d{2,4})', r'\1 \2', result)
    result = re.sub(r'(\d{2}/\d{2}/\d{2,4})([А-Яа-яA-Za-z])', r'\1 \2', result)
    
    # 10. Пробел перед номером (цифрами), если перед ними буква
    result = re.sub(r'([А-Яа-яA-Za-z])(\d)', r'\1 \2', result)
    
    # 11. Пробел после номера, если после них буква
    result = re.sub(r'(\d)([А-Яа-яA-Za-z])', r'\1 \2', result)
    
    # 12. Пробел перед тире (если нет пробела)
    result = re.sub(r'([^ ])-([А-Яа-яA-Za-z])', r'\1 - \2', result)
    result = re.sub(r'([А-Яа-яA-Za-z])-([^ ])', r'\1 - \2', result)
    
    # 13. Пробел после открывающей скобки
    result = re.sub(r'\(([А-Яа-яA-Za-z])', r'(\1', result)
    
    # 14. Пробел перед закрывающей скобкой
    result = re.sub(r'([А-Яа-яA-Za-z])\)', r'\1) ', result)
    
    # 15. Убираем лишние пробелы
    result = re.sub(r' +', ' ', result)
    
    # 16. Убираем пробелы перед знаками препинания
    result = re.sub(r' ([.,;:])', r'\1', result)
    
    # 17. Убираем пробел после открывающей скобки (если он есть)
    result = re.sub(r'\( ', r'(', result)
    
    # 18. Убираем пробел перед закрывающей скобкой (если он есть)
    result = re.sub(r' \)', r')', result)
    
    return result.strip()